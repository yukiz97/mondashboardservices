package monservice.models;

public class DynamicSVGMapItemBean {
	String value;
	String name;
	float cx;
	float cy;
	
	public String getValue() {
		return value;
	}
	public void setValue(String value) {
		this.value = value;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public float getCx() {
		return cx;
	}
	public void setCx(float cx) {
		this.cx = cx;
	}
	public float getCy() {
		return cy;
	}
	public void setCy(float cy) {
		this.cy = cy;
	}
}
